from src.algorithm.utils import find_max


def test_find_max_passed() -> None:
    bids = {
        1: {1: 100, 2: 100, 4: 100},
        2: {2: 150, 5: 150},
        3: {1: 200, 5: 200},
        4: {
            3: 250,
            4: 250,
        },
        5: {2: 300, 3: 300, 5: 300},
        6: {2: 350, 5: 350},
        7: {
            1: 400,
            4: 400,
        },
        8: {2: 450, 5: 450},
        9: {1: 500, 3: 500, 5: 500},
        10: {2: 550, 3: 550},
    }

    res = find_max(bids)

    exected = {1: 100, 2: 150, 3: 200, 4: 250, 5: 300, 6: 350, 7: 400, 8: 450, 9: 500, 10: 550}

    assert res == exected
